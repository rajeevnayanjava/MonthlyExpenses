package ifElse;

public class IfEleseExample {

	public static void main(String[] args) {
		// printing odd and even numbers between 1 to10
		int i=1;
		while(i<=10){
			if(i%2==0){
				System.out.println(i+" is even number.");
			}
			else{
				System.out.println(i+" is odd number");
			}
			i++;
		}

	}

}
